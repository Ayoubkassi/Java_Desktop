        private void updateTable(){
            try{
                //get rows that i need with HandleDB
                int id =0;
                DefaultTableModel model = (DefaultTableModel) tblJobs.getModel();

                for(EmploiJob job : offres){
                    
                    Object[] row = new Object[4];
                    row[0] = ++id;
                    row[1] = job.getTitle();
                    row[2] = job.getTypeContart();
                    row[3] = job.getVille();

                    model.addRow(row);
                }
                
            }catch(Exception ex){
              ex.printStackTrace();
            }
        }
        
       private void fetch() {
       jobs.clear();
       
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/java", "root", "guillaume");
            String sql = "select * from Jobs";
            st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                Job job = new Job(rs.getString("title"), rs.getString("company"), rs.getString("link"));
                //job.setId(rs.getInt("id"));
                //System.out.println(job.getId());
                jobs.add(job);
            }
            DefaultTableModel model = (DefaultTableModel) tblJobs.getModel();
            for (Job job : jobs) {

                Object[] row = new Object[4];
                job.setId(job.getId()+1);
                row[0] = job.getId();
                row[1] = job.getTitle();
                row[2] = job.getCompany();
                row[3] = job.getLink();

                model.addRow(row);
            }

        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Work.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
